static void render_current_wpm(void) {
    char wpm_str[5];
    oled_set_cursor(0, 14);

    sprintf(wpm_str, " %03d", get_current_wpm());
    oled_write(wpm_str, false);

    oled_set_cursor(0, 15);
    oled_write(" wpm", false);
}

bool render_status(void) {
    oled_set_cursor(0, 0);
    if (keymap_config.swap_lctl_lgui) {
        oled_write_raw_P(windows_logo, sizeof(windows_logo));
    } else {
        oled_write_raw_P(mac_logo, sizeof(mac_logo));
    }

    oled_set_cursor(0, 3);

    switch (get_highest_layer(default_layer_state)) {
        case _QWERTY:
            oled_write("QWRTY", false);
            break;
        case _HALMAK:
            oled_write("HALMK ", false);
            break;
        default:
            oled_write("UNDEF", false);
    }

    oled_set_cursor(0, 4);
    oled_write_raw_P(line, sizeof(line));
    oled_set_cursor(0, 5);

    switch (get_highest_layer(layer_state)) {
        case _QWERTY:
        case _HALMAK:
            oled_write("Base ", false);
            break;
        case _NAV:
            oled_write("Nav  ", false);
            break;
        case _MOUSE:
            oled_write("Mouse", false);
            break;
        case _BUTTON:
            oled_write("Btn  ", false);
            break;
        case _MEDIA:
            oled_write("Media", false);
            break;
        case _NUMBER:
            oled_write("Num  ", false);
            break;
        case _SYMBOL:
            oled_write("Sym  ", false);
            break;
        case _FUNCTION:
            oled_write("Fn   ", false);
            break;
        case _CONTROL:
            oled_write("Ctrl ", false);
            break;
        default:
            // Or use the write_ln shortcut over adding '\n' to the end of your string
            oled_write("Undef", false);
    }

    oled_set_cursor(0, 7);

    // Host Keyboard LED Status
    oled_write(led_usb_state.num_lock ? "NUMLK" : "     ", false);
    oled_write(led_usb_state.caps_lock ? "CPSLK" : "     ", false);
    oled_write(led_usb_state.scroll_lock ? "SCRLK" : "     ", false);

    return false;
}

bool oled_task_user(void) {
    current_wpm   = get_current_wpm();
    led_usb_state = host_keyboard_led_state();

    if (is_keyboard_master()) {
        render_status();
        render_luna(0, 13);
    } else {
        render_logo();
        render_girl();
        render_current_wpm();
    }
    return false;
}

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
        /* KEYBOARD PET STATUS START */
        case KC_LCTL:
        case KC_RCTL:
            if (record->event.pressed) {
                isSneaking = true;
            } else {
                isSneaking = false;
            }
            break;
        case KC_SPC:
            if (record->event.pressed) {
                isJumping  = true;
                showedJump = false;
            } else {
                isJumping = false;
            }
            break;
        case E_CUT:
            if (record->event.pressed) {
                if (keymap_config.swap_lctl_lgui) {
                    register_mods(mod_config(MOD_LCTL));
                    register_code(KC_X);
                } else {
                    register_mods(mod_config(MOD_LGUI));
                    register_code(KC_X);
                }
            } else {
                if (keymap_config.swap_lctl_lgui) {
                    unregister_mods(mod_config(MOD_LCTL));
                    unregister_code(KC_X);
                } else {
                    unregister_mods(mod_config(MOD_LGUI));
                    unregister_code(KC_X);
                }
            }
            break;
       case E_COPY:
            if (record->event.pressed) {
                if (keymap_config.swap_lctl_lgui) {
                    register_mods(mod_config(MOD_LCTL));
                    register_code(KC_C);
                } else {
                    register_mods(mod_config(MOD_LGUI));
                    register_code(KC_C);
                }
            } else {
                if (keymap_config.swap_lctl_lgui) {
                    unregister_mods(mod_config(MOD_LCTL));
                    unregister_code(KC_C);
                } else {
                    unregister_mods(mod_config(MOD_LGUI));
                    unregister_code(KC_C);
                }
            }
            break;
       case E_PASTE:
            if (record->event.pressed) {
                if (keymap_config.swap_lctl_lgui) {
                    register_mods(mod_config(MOD_LCTL));
                    register_code(KC_V);
                } else {
                    register_mods(mod_config(MOD_LGUI));
                    register_code(KC_V);
                }
            } else {
                if (keymap_config.swap_lctl_lgui) {
                    unregister_mods(mod_config(MOD_LCTL));
                    unregister_code(KC_V);
                } else {
                    unregister_mods(mod_config(MOD_LGUI));
                    unregister_code(KC_V);
                }
            }
            break;
       case E_UNDO:
            if (record->event.pressed) {
                if (keymap_config.swap_lctl_lgui) {
                    register_mods(mod_config(MOD_LCTL));
                    register_code(KC_Z);
                } else {
                    register_mods(mod_config(MOD_LGUI));
                    register_code(KC_Z);
                }
            } else {
                if (keymap_config.swap_lctl_lgui) {
                    unregister_mods(mod_config(MOD_LCTL));
                    unregister_code(KC_Z);
                } else {
                    unregister_mods(mod_config(MOD_LGUI));
                    unregister_code(KC_Z);
                }
            }
            break;
       case E_REDO:
            if (record->event.pressed) {
                if (keymap_config.swap_lctl_lgui) {
                    register_mods(mod_config(MOD_LCTL));
                    register_code(KC_Y);
                } else {
                    register_mods(mod_config(MOD_LGUI));
                    register_code(KC_Y);
                }
            } else {
                if (keymap_config.swap_lctl_lgui) {
                    unregister_mods(mod_config(MOD_LCTL));
                    unregister_code(KC_Y);
                } else {
                    unregister_mods(mod_config(MOD_LGUI));
                    unregister_code(KC_Y);
                }
            }
            break;
    }
    return true;
}

